package Step03;

import java.util.*;

// 코딩은 체육과목 입니다 - 코딩 공부를 잘 하여 이렇게 long long long long...을 칠판에 적는 일이 없도록 합시다.
public class st03_25314 {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int n = in.nextInt();
		
		for(int i = 0; i < n / 4; i++) {
			System.out.print("long ");
		}
		
		System.out.println("int");
		
		in.close();
	}

}
